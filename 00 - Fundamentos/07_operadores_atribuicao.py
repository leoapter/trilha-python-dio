# Operadores de Atribuição, atribuem ou alteram o valor de uma variável

saldo = 500  # Atribuição simples de valor, à variável
print(saldo)

saldo = 200  # Atribuição de novo valor à variável
print(saldo)

saldo += 10  # Atribui novo valor, aapós dicionar 10 ao valor da variável
print(saldo)

saldo -= 5   # Atribui novo valor, após subtrair 5 do valor da variável
print(saldo)

saldo //= 2  # Atribui novo valor, após divisão inteira da variavel por 2.
print(saldo)

saldo /= 2   # Divide o valor da variável por 2. Divisão real. Retorna um float
print(saldo)

saldo *= 10  # Multiplica o valor da variável por 10
print(saldo)

saldo %= 4   # Resto da divisão do valor da variável por 4
print(saldo)

saldo **= 2  # Eleva o valor da variável à potência de 2
print(saldo)
