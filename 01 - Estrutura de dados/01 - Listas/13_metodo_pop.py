# Metodo Pop
# Utilizado para remover o ultimo objeto da lista
# No Python, uma lista se comporta como uma pilha de pratos
# O Metodo Pop retira 1 prato de cima da pilha por vez


linguagens = ["python", "js", "c", "java", "csharp"]
print(linguagens)
print(linguagens.pop())  # csharp
print(linguagens)
print(linguagens.pop())  # java
print(linguagens)
print(linguagens.pop())  # c
print(linguagens)
print(linguagens.pop(0))  # python
print(linguagens)
